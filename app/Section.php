<?php

namespace App;

use Illuminate\Database\Eloquent\Model;

class Section extends Model
{
    
    // =============================================================================
    // QUERIES
    // =============================================================================

    // =============================================================================
    // VALIDATIONS
    // =============================================================================

    // =============================================================================
    // UTILITIES
    // =============================================================================

    public static function createFromData($data)
    {
    	$section = new self();
    	$section->name = array_get($data, 'name');
    	$section->save();
    	return $section;
    }


